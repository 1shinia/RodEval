const translations: Record<Locale, Dict> = { en, zh }

export function lookupTranslation(locale: Locale, path: string, params?: Record<string, string | number>): string {
  const keys = path.split('.')
  let node: string | Dict = translations[locale]
  for (const key of keys) {
    if (typeof node === 'string') return path
    node = (node as Dict)[key]
    if (node === undefined) return path
  }
  const raw = typeof node === 'string' ? node : path
  if (!params) return raw
  return raw.replace(/\$\{(\w+)\}/g, (_, k) => String(params[k] ?? `$\{${k}}`))
}
